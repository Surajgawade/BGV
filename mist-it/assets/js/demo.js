$(document).on('change', '#filter_by_status', function(){
    var status = $("#filter_by_status option:selected").html();
    
    if(status == 'Closed' || status == 'Insufficiency')
    {
      $('#date_field_show_hide').show();
    }
    else
    {
      $('#date_field_show_hide').hide();
    }
    $.ajax({
        type:'POST',
        data:'status='+status,
        url : 'candidates/sub_status_list',
        beforeSend :function(){
            jQuery('#filter_by_sub_status').find("option:eq(0)").html("Please wait..");
        },
        complete:function(){
            jQuery('#filter_by_sub_status').find("option:eq(0)").html("Sub Status");
        },
        success:function(jdata) {
            $('#filter_by_sub_status').empty();
            $.each(jdata.sub_status_list, function(key, value) {
              $('#filter_by_sub_status').append($("<option></option>").attr("value",key).text(value));
            });
        }
    });
});

